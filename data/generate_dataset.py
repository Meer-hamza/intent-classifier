# Build the dataset with augmentation
rows = []
for intent, messages in intent_data.items():
    for msg in messages:
        rows.append({"text": msg, "intent": intent})
        # Simple augmentation: lowercase variant
        rows.append({"text": msg.lower(), "intent": intent})
        # Simple augmentation: add filler prefix
        fillers = ["Hi, ", "Hello, ", "Hey, ", "Excuse me, ", ""]
        filler = random.choice(fillers)
        if filler:
            rows.append({"text": filler + msg[0].lower() + msg[1:], "intent": intent})

# Shuffle
random.shuffle(rows)

df = pd.DataFrame(rows).drop_duplicates(subset="text").reset_index(drop=True)
print(f"Total samples: {len(df)}")
print(df["intent"].value_counts())

# NEW (saves in the same folder as the script)
df.to_csv("data/intents_dataset.csv", index=False)
